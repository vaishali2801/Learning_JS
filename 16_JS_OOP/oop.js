// 4 OOP principles for class design:

// 1. **Abstraction** => Abstraction means hiding certain details that don't matter to the user and only showing essential features or functions.
// Example of this:
// FrontCamOn(), FrontCamOff() => front Camera

// 2. **Encapsulation** => Encapsulation means keeping properties and methods private inside a class, so that they are not accessible from outside that class.

// 3. **Inheritance** => Inheritance allows us to access parent class properties and methods in a child class.

// 4. **Polymorphism** => Polymorphism means having many different forms. We can overwrite a method inherited from a parent class